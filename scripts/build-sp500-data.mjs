#!/usr/bin/env node
/**
 * Fetches S&P 500 constituents and generates src/lib/sp500.ts
 * Run: node scripts/build-sp500-data.mjs
 */

import { writeFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const CSV_URL =
  "https://raw.githubusercontent.com/datasets/s-and-p-500-companies/master/data/constituents.csv";

const SECTOR_MAP = {
  Energy: { pokemonType: "fire", emoji: "🔥", hint: (n) => `${n} — energy & fuel company` },
  Materials: { pokemonType: "steel", emoji: "⚙️", hint: (n) => `${n} — makes metals & materials` },
  Industrials: { pokemonType: "fighting", emoji: "💪", hint: (n) => `${n} — builds machines & infrastructure` },
  "Consumer Discretionary": {
    pokemonType: "normal",
    emoji: "🛍️",
    hint: (n) => `${n} — sells things people want for fun`,
  },
  "Consumer Staples": {
    pokemonType: "grass",
    emoji: "🌿",
    hint: (n) => `${n} — everyday food & household essentials`,
  },
  "Health Care": { pokemonType: "fairy", emoji: "💊", hint: (n) => `${n} — health & medicine company` },
  Financials: { pokemonType: "psychic", emoji: "🏦", hint: (n) => `${n} — bank, insurance, or finance` },
  "Information Technology": {
    pokemonType: "electric",
    emoji: "⚡",
    hint: (n) => `${n} — tech, software, or chips`,
  },
  "Communication Services": {
    pokemonType: "psychic",
    emoji: "📡",
    hint: (n) => `${n} — media, telecom, or entertainment`,
  },
  Utilities: { pokemonType: "water", emoji: "💧", hint: (n) => `${n} — power, water, or gas utility` },
  "Real Estate": { pokemonType: "dragon", emoji: "🏠", hint: (n) => `${n} — owns buildings & property` },
};

function parseCsv(text) {
  const lines = text.trim().split("\n");
  const rows = [];
  for (let i = 1; i < lines.length; i++) {
    const line = lines[i];
    const parts = [];
    let cur = "";
    let inQuotes = false;
    for (let j = 0; j < line.length; j++) {
      const ch = line[j];
      if (ch === '"') {
        inQuotes = !inQuotes;
      } else if (ch === "," && !inQuotes) {
        parts.push(cur);
        cur = "";
      } else {
        cur += ch;
      }
    }
    parts.push(cur);
    if (parts.length < 4) continue;
    rows.push({
      ticker: parts[0].trim(),
      name: parts[1].trim(),
      sector: parts[2].trim(),
      subIndustry: parts[3].trim(),
    });
  }
  return rows;
}

function escape(str) {
  return str.replace(/\\/g, "\\\\").replace(/"/g, '\\"').replace(/\n/g, " ");
}

async function main() {
  const res = await fetch(CSV_URL);
  if (!res.ok) throw new Error(`Failed to fetch S&P 500 CSV: ${res.status}`);
  const csv = await res.text();
  const rows = parseCsv(csv);

  const companies = rows.map((row) => {
    const meta = SECTOR_MAP[row.sector] ?? {
      pokemonType: "normal",
      emoji: "⭐",
      hint: (n) => `${n} — S&P 500 company`,
    };
    return {
      ticker: row.ticker,
      name: row.name,
      sector: row.sector,
      subIndustry: row.subIndustry,
      pokemonType: meta.pokemonType,
      emoji: meta.emoji,
      hint: meta.hint(row.name),
    };
  });

  const tickers = companies.map((c) => c.ticker);

  const ts = `import type { PokemonType } from "@/types/card";

/** S&P 500 constituent — auto-generated by scripts/build-sp500-data.mjs */
export interface SP500Company {
  ticker: string;
  name: string;
  sector: string;
  subIndustry: string;
  pokemonType: PokemonType;
  emoji: string;
  hint: string;
}

/** ${companies.length} companies in the S&P 500 index (as of dataset fetch). */
export const SP500_COMPANIES: SP500Company[] = [
${companies
  .map(
    (c) =>
      `  { ticker: "${escape(c.ticker)}", name: "${escape(c.name)}", sector: "${escape(c.sector)}", subIndustry: "${escape(c.subIndustry)}", pokemonType: "${c.pokemonType}", emoji: "${c.emoji}", hint: "${escape(c.hint)}" }`
  )
  .join(",\n")}
];

export const SP500_TICKERS: string[] = [
${tickers.map((t) => `  "${escape(t)}"`).join(",\n")}
];

const SP500_SET = new Set(SP500_TICKERS);

export function isSP500Ticker(ticker: string): boolean {
  return SP500_SET.has(ticker.toUpperCase());
}

export function getSP500Company(ticker: string): SP500Company | undefined {
  const sym = ticker.toUpperCase();
  return SP500_COMPANIES.find((c) => c.ticker === sym);
}

export function searchSP500(query: string, limit = 20): SP500Company[] {
  const q = query.trim().toUpperCase();
  if (!q) return [];
  const matches = SP500_COMPANIES.filter(
    (c) => c.ticker.includes(q) || c.name.toUpperCase().includes(q)
  );
  return matches.slice(0, limit);
}
`;

  const outPath = resolve(ROOT, "src/lib/sp500.ts");
  writeFileSync(outPath, ts, "utf8");
  console.log(`Wrote ${companies.length} companies to ${outPath}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});